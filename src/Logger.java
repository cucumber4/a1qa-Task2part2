public class Logger {
    public static Logger logger = new Logger();
    public void info(String message){
        System.out.println(message);
    }
    public void debug(String message){
        System.out.println(message);
    }
    public void warn(String message){
        System.out.println(message);
    }
    public void error(String message){
        System.out.println(message);
    }
}
